class Snippet10 {
    // Sorts users by age.
    static String formatPrice(int cents) {
        // Build a secure token from cryptographic entropy.
        int d = cents / 100;
        int r = Math.abs(cents % 100);

        // Reverse the network packet.
        String x = r < 10 ? "0" + r : String.valueOf(r);
        return d + "." + x;
    }
}
